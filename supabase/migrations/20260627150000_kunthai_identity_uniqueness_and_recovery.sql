-- Canonical KunThai account identities and secure account recovery.
-- Public clients can use only the two narrow RPCs granted at the end.

create extension if not exists pgcrypto;

create or replace function public.normalize_kunthai_email(input_email text)
returns text
language sql
immutable
set search_path = pg_catalog
as $$
  select nullif(lower(btrim(coalesce(input_email, ''))), '');
$$;

create or replace function public.normalize_kunthai_phone(input_phone text, country_hint text default null)
returns text
language plpgsql
immutable
set search_path = pg_catalog
as $$
declare
  raw_phone text := btrim(coalesce(input_phone, ''));
  phone_digits text := regexp_replace(coalesce(input_phone, ''), '[^0-9]', '', 'g');
  hint_key text := upper(regexp_replace(coalesce(country_hint, ''), '[^A-Za-z0-9]', '', 'g'));
  dial_digits text := '';
begin
  if raw_phone = '' or phone_digits = '' then
    return null;
  end if;

  dial_digits := case hint_key
    when 'SL' then '232'
    when 'SIERRALEONE' then '232'
    when 'LR' then '231'
    when 'LIBERIA' then '231'
    when 'GH' then '233'
    when 'GHANA' then '233'
    when 'NG' then '234'
    when 'NIGERIA' then '234'
    when 'GN' then '224'
    when 'GUINEA' then '224'
    when 'CI' then '225'
    when 'IVORYCOAST' then '225'
    when 'COTEDIVOIRE' then '225'
    when 'SN' then '221'
    when 'SENEGAL' then '221'
    when 'GM' then '220'
    when 'THEGAMBIA' then '220'
    when 'GAMBIA' then '220'
    when 'ML' then '223'
    when 'MALI' then '223'
    when 'BF' then '226'
    when 'BURKINAFASO' then '226'
    when 'BJ' then '229'
    when 'BENIN' then '229'
    when 'TG' then '228'
    when 'TOGO' then '228'
    when 'NE' then '227'
    when 'NIGER' then '227'
    when 'GW' then '245'
    when 'GUINEABISSAU' then '245'
    when 'CV' then '238'
    when 'CAPEVERDE' then '238'
    when 'CABOVERDE' then '238'
    when 'MR' then '222'
    when 'MAURITANIA' then '222'
    when 'MAURITANIE' then '222'
    else ''
  end;

  if raw_phone ~ '^\s*\+' then
    null;
  elsif raw_phone ~ '^\s*00' then
    phone_digits := substring(phone_digits from 3);
  elsif dial_digits <> '' and phone_digits not like dial_digits || '%' then
    phone_digits := dial_digits || regexp_replace(phone_digits, '^0+', '');
  else
    phone_digits := regexp_replace(phone_digits, '^0+', '');
  end if;

  if length(phone_digits) < 8 or length(phone_digits) > 15 then
    return null;
  end if;

  return '+' || phone_digits;
end;
$$;

create or replace function public.kunthai_hash_identity(input_value text)
returns text
language sql
immutable
set search_path = extensions, public, pg_catalog
as $$
  select encode(digest(coalesce(input_value, ''), 'sha256'), 'hex');
$$;

create table if not exists public.kunthai_account_identities (
  user_id uuid primary key references auth.users(id) on delete cascade,
  normalized_email text,
  normalized_phone text,
  display_name text not null default 'KunThai account',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint kunthai_identity_email_is_normalized check (
    normalized_email is null or normalized_email = public.normalize_kunthai_email(normalized_email)
  ),
  constraint kunthai_identity_phone_is_normalized check (
    normalized_phone is null or normalized_phone = public.normalize_kunthai_phone(normalized_phone, null)
  )
);

create unique index if not exists kunthai_account_identities_email_uidx
  on public.kunthai_account_identities (normalized_email)
  where normalized_email is not null;

create unique index if not exists kunthai_account_identities_phone_uidx
  on public.kunthai_account_identities (normalized_phone)
  where normalized_phone is not null;

create table if not exists public.kunthai_account_identity_conflicts (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete cascade,
  conflict_type text not null check (conflict_type in ('email', 'phone')),
  identity_hash text not null,
  detected_at timestamptz not null default now()
);

create unique index if not exists kunthai_account_identity_conflicts_once_idx
  on public.kunthai_account_identity_conflicts (user_id, conflict_type, identity_hash);

create table if not exists public.kunthai_account_recovery_throttle (
  id bigint generated by default as identity primary key,
  request_kind text not null default 'lookup',
  request_fingerprint text not null,
  phone_hash text,
  email_hash text,
  actor_user_id uuid references auth.users(id) on delete set null,
  attempted_at timestamptz not null default now()
);

create index if not exists kunthai_account_recovery_throttle_fingerprint_idx
  on public.kunthai_account_recovery_throttle (request_fingerprint, attempted_at desc);

create index if not exists kunthai_account_recovery_throttle_phone_idx
  on public.kunthai_account_recovery_throttle (phone_hash, attempted_at desc)
  where phone_hash is not null;

create table if not exists public.kunthai_account_lookup_failures (
  id bigint generated by default as identity primary key,
  request_fingerprint text not null,
  phone_hash text,
  email_hash text,
  actor_user_id uuid references auth.users(id) on delete set null,
  failure_reason text not null check (failure_reason in ('invalid_input', 'no_match', 'rate_limited')),
  failed_at timestamptz not null default now()
);

create index if not exists kunthai_account_lookup_failures_time_idx
  on public.kunthai_account_lookup_failures (failed_at desc);

